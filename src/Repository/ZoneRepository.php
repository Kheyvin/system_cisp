    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Zone::class);
    }

    // Método para obtener los planes por abbreviation de zona
    public function findPlansByZoneAbbreviation(string $abbreviation)
    {
        return $this->createQueryBuilder('z')
            ->innerJoin('z.plans', 'p') // Relación entre Zone y Plan
            ->where('z.abbreviation = :abbreviation')
            ->setParameter('abbreviation', $abbreviation)
            ->getQuery()
            ->getResult();
    }

